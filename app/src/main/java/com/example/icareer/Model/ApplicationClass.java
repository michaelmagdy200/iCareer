package com.example.icareer.Model;

public class ApplicationClass {
    private String application_name ;
    private String application_email ;
    private String application_phone ;
    private String application_skills ;
    private String application_education ;
    private String application_summary ;
    private String application_id ;
    public ApplicationClass() {
    }

    public String getApplication_name() {
        return application_name;
    }

    public void setApplication_name(String application_name) {
        this.application_name = application_name;
    }

    public String getApplication_email() {
        return application_email;
    }

    public void setApplication_email(String application_email) {
        this.application_email = application_email;
    }

    public String getApplication_phone() {
        return application_phone;
    }

    public void setApplication_phone(String application_phone) {
        this.application_phone = application_phone;
    }

    public String getApplication_skills() {
        return application_skills;
    }

    public void setApplication_skills(String application_skills) {
        this.application_skills = application_skills;
    }

    public String getApplication_education() {
        return application_education;
    }

    public void setApplication_education(String application_education) {
        this.application_education = application_education;
    }

    public String getApplication_summary() {
        return application_summary;
    }

    public void setApplication_summary(String application_summary) {
        this.application_summary = application_summary;
    }

    public String getApplication_id() {
        return application_id;
    }

    public void setApplication_id(String application_id) {
        this.application_id = application_id;
    }
}
